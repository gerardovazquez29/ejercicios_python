
#*Crea un programa que imprima los números pares del 1 al 20 usando un bucle for
for i in range(1, 21):
    if i % 2 == 0:
        print(i)
#*Crea un programa que imprima los números impares del 1 al 20 usando un bucle while
i = 1
while i <= 20:
    if i % 2 != 0:
        print(i)
    i += 1  
